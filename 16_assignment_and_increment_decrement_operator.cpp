#include <iostream>
using namespace std;

/* run this program using the console pauser or add your own getch, system("pause") or input loop */


int main(int argc, char** argv) {
	
	int a = 10;
	
	a += 5;
	
	cout<<a<<endl;
	
	
	//pre increment 
	
	int val = 5;
	cout<<++val<<endl;
	
	//post increment 
	int val1 = 10;
	cout<<val1++<<endl;
	cout<<val1<<endl;

}
